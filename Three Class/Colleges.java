public class Colleges{
	private String name;
	private	int num;
	private	int area;

	public void setName(String name){
		this.name = name;
	}

	public String getName(){
		return this.name;
	}

	public void setNum(int num){
		this.num = num;
	}

	public int getNum(){
		return this.num;
	}

	public void setArea(int area){
		this.area = area;
	}

	public int getArea(){
		return this.area;
	}
}



